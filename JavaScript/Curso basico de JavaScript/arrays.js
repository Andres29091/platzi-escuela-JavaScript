let frutas = ["Manzana", "Platano", "Cereza", "Fresa"];
console.log(frutas);

/* longitud del array */
frutas.length();

/* Insertar un elemento al final de un array y retorna la longitud */
frutas.push();

/* Quitar el ultimo elemento del array y retorna el elemento */
frutas.pop();

/* Insertar elemento al principio del array y retorna la longitud */
frutas.unshift();

/* Elimina el primer elemento del array y lo retorna */
frutas.shift();